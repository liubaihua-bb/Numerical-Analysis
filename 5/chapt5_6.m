function [a, b, c] = chapt5_6
    % 输入数据
    x = [1953 1964 1982 1990 2000]';
    y = [5.82 6.95 10.08 11.34 12.66]';

    % 构建设计矩阵
    A = [ones(5, 1) x -x .* y];

    % 用最小二乘法求解线性回归系数
    z = A \ y;
    a = z(1);
    b = z(2);
    c = z(3);

    % 生成用于绘图的数据点
    v = linspace(1953, 2000, 100);

    % 绘制原始数据和拟合曲线
    plot(x, y, 'b-+', v, (a + b * v) ./ (1 + c * v), 'k-');
end



